<tr><td valign="top"><h2>Le vid&eacute;oprojecteur pour les nuls</h2>


<p align="justify">

<b>Projecteur</b> n.m. Appareil envoyant au loin un puissant faisceau de rayons lumineux.<br>
<b>Vid&eacute;o</b> adj. Se dit des signaux servant &agrave; la transmission d'images, et des appareils, des installations qui utilisent ces signaux.<br><br>

Un vid&eacute;oprojecteur est donc un appareil qui reproduit une source vid&eacute;o et proj&egrave;te les images sur un &eacute;cran ou un mur blanc.<br><br>

<font color="#FF7F00"><b>Pourquoi choisir un vid&eacute;oprojecteur ?</b></font>

<br><br>
C'est <u>la seule</u> solution pour avoir un -vraiment- grand &eacute;cran &agrave; la maison (voir plus bas).<br>
En effet, n'importe quel vid&eacute;oprojecteur permet d'avoir une image de 2 m&egrave;tres de diagonale... et plus !<br>
En second lieu, lorsque vous ne l'utilisez pas, vous n'aurez pas &agrave; voir un grand &eacute;cran &eacute;teint, le seul vid&eacute;oprojecteur a la taille d'une boite &agrave; chaussures.

<br><br>

<font color="#FF7F00"><b>Ce qu'il faut savoir :</b></font><br><br>

<font color="#FF7F00">&#149;</font> Une source vid&eacute;o (tuner, d&eacute;codeur TNT, lecteur DVD) doit &egrave;tre connect&eacute;e au vid&eacute;oprojecteur<br>
(Quelques rares mod&egrave;les ont un lecteur DVD ou un tuner int&eacute;gr&eacute;).<br><br>
<font color="#FF7F00">&#149;</font> Ne comptez pas sur le vid&eacute;oprojecteur pour avoir du son.<br>
Certes il est &eacute;quip&eacute; d'un petit haut-parleur, mais il faudra vous &eacute;quiper d'un syst&egrave;me de son.<br><br>

<font color="#FF7F00">&#149;</font> La lampe est LE consommable du vid&eacute;oprojecteur, elle dure en moyenne normalement 2000 heures et co&ucirc;te 2 &agrave; 300 euros en moyenne. On ne laissera donc pas le vid&eacute;oprojecteur allum&eacute; comme une t&eacute;l&eacute;vision...

<br><br>

<font color="#FF7F00">&#149;</font> Vous devez avoir un peu de recul entre le mur (ou l'&eacute;cran) o&ugrave; vous projetez de projeter (joli, &ccedil;a !) et le vid&eacute;oprojecteur.<br>
Plus le recul est important, plus vous pourrez avoir une grande image. par exemple :<br><br><center>

