<font color="#FF7F00"><b>Choisir son projecteur :</b></font><br><br>

Les points les plus importants &agrave; prendre en compte sont :<br><br>
<font color="#FF7F00">&#149;</font> La r&eacute;solution<br>
Elle s'exprime par deux chiffres, par exemple (800 x600) le premier chiffre est le nombre de pixels horizontaux et le second le nombre de pixels verticaux. Plus ces nombres sont &eacutelev&eacute;s, plus les d&eacute;tails sont nets.<br><br>

<font color="#FF7F00">&#149;</font> La luminosit&eacute;<br>
Elle s'exprime en Lumens ANSI, et plus elle est importante plus vous pourrez utiliser le projecteur sans faire le noir dans la pi&egrave;ce. Si vous pr&eacute;voyez une grande taille d'image (3m et +), il vous faut une luminosit&eacute; &eacute;lev&eacute;e.<br><br>

<font color="#FF7F00">&#149;</font> Le contraste<br>
Le rapport de contraste (par exemple 1000:1 ou 2000:1) exprime le rapport entre les valeurs de noir et de blanc. Pour une utilisation dans le noir en home-cinema, un rapport de 800:1 est suffisant.<br><br>

<font color="#FF7F00">&#149;</font> Le bruit<br>
Les ventilateurs des vid&eacute;oprojecteurs tournent en permanence, si l'appareil est situ&eacute; tout pr&egrave;s de vous, le bruit peut s'av&eacute;rer tr&egrave;s g&ecirc;nant. Les appareils modernes sont de plus en plus sliencieux, mais &ccedil;a peut &ecirc;tre un crit&egrave;re de choix si vous h&eacute;sitez entre deux mod&egrave;les.<br><br>

<font color="#FF7F00">&#149;</font> Le prix de la lampe<br>
Cela ne doit pas &ecirc;tre un crit&egrave;re essentiel pour faire votre choix, mais il ne faut pas le n&eacute;gliger. En effet, &agrave; performances et prix identiques, entre deux vid&eacute;oprojecteurs dont les lampes co&ucirc;tent du simple au double, lequel choisirez-vous ?<br><br>

