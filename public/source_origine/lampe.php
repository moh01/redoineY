<tr><td colspan="2"><h2>Le videoprojecteur et sa lampe</h2></td></tr>

<tr>
<td><font color="#FF7F00">La lampe du videoprojecteur</font><br><br>

<p align="justify">

La lampe du videoprojecteur produit une lumi&egrave;re blanche qui est dirig&eacute;e vers les miroirs de la puce DMD.
<br><br>
Cette lumi&egrave;re blanche (qui contient toutes les couleurs, comme celle du jour) traverse la roue chromatique divis&eacute;e en s&eacute;gments rouge, vert et bleu.
<br><br>
Les miroirs du circuit DMD (command&eacute; par le processeur) pivotent pour renvoyer la lumi&egrave;re vers l'&eacute;cran, ou ne pas la renvoyer lorsque'un pixel doit rester noir.


<br><br><br>

<b>Manipulation</b><br>
Le remplacement du module de lampe du videoprojecteur est rapide, facile et ne n&eacute;cessite ni aptitudes ni connaissances particuli&egrave;res.
L'ampoule est prot&eacute;g&eacute;e dans son module. C'est la raison pour laquelle il est conseill&eacute; de ne remplacer la lampe que par le module de lampe complet (sauf sp&eacute;cifications contraires dûes au fabricant du vid&eacute;o-projecteur).
<br><br>

En revanche, ne remplacer que l'ampoule (bulb) est nettement plus d&eacute;licat : Ne jamais toucher l'ampoule avec les doigts, l'usage d'un tissu ou de gants est recommand&eacute; pour toute manipulation en contact avec le verre de l'ampoule.<br>

<br>
Nettoyez les filtres &agrave; air du projecteur. Leur bloquage cause une surchauffe de la lampe qui entraine sa d&eacute;faillance. Les fabricants peuvent en tester les causes et invalidera toute garantie. Assurez-vous toujours du bon &eacute;coulement d'air autour du projecteur.
<br>
Si vous remplacez une ampoule &agrave; l'int&eacute;rieur m&ecirc;me du module de logement, assurez vous que ses bornes ne sont pas sur-serr&eacute;es afin d'&eacute;viter une fracture de la c&eacute;ramique et, par cons&eacute;quent, entraîner une r&eacute;duction de l'isolation &eacute;lectronique de la lampe. D'une autre part, un sous-serrage des bornes peut contribuer aux conditions n&eacute;cessaires au jaillissement d'un arc &eacute;l&eacute;ctrique et de ce fait entrainer une surcharge &eacute;l&eacute;ctrique sur le ballast pouvant r&eacute;sulter en une panne. 
<br><br>
Pour installer les ampoules, les lampes doivent &ecirc;tre positionn&eacute;es correctement. On a tendance &agrave; s'arr&ecirc;ter au premier signe de r&eacute;sistance, cependant continuez &agrave; presser doucement jusqu'&agrave; ce que la lampe soit bien emboit&eacute;e.
<br><br>
<b>Eteindre le projecteur</b><br>

Utilisez toujours la t&eacute;l&eacute;commande du projecteur pour l' &eacute;teindre. Ne d&eacute;brancher jamais le projecteur pour l'&eacute;teindre. Dans l'absence d'images &agrave; projeter, le projecteur adoptera un mode automatique de refroidissement et actionera son ventilateur afin de refroidir d'une mani&egrave;re contrôl&eacute;e sa lampe. La temp&eacute;rature d'une lampe en &eacute;tat op&eacute;rationnel atteind des milliers de degr&eacute;s. D&eacute;brancher un projecteur sans permettre &agrave; ce dernier de contrôler le refroidissement de sa lampe (comme indiqu&eacute; ci-dessus) causera un refroidissement in&eacute;gal de la surface de la lampe, et aboutira &agrave; son usure pr&eacute;matur&eacute;e et ce sera la panne.
<br><br>
<b>Chocs et vibrations</b><br>

Le non-fonctionnement d'une lampe peut &ecirc;tre caus&eacute; par au choc physique (m&eacute;canique). Un filament de lampe chaud est tr&egrave;s fragile, vous devriez donc toujours vous assurer que l'appareil est &eacute;teint et attendre le refroidissement de la lampe.
<br><br>
<b>Le mode Economie</b><br>

Si votre projecteur est &eacute;quip&eacute; d'un commutateur de lampe "Normal/Eco", vous pouvez prolonger la dur&eacute;e de vie moyenne de la lampe en utilisant la position "Eco" d&egrave;s que possible.
<br><br>

<b>Nettoyage du vid&eacute;oprojecteur</b><br>
D&eacute;montez et aspirez les filtres comme indiqu&eacute; dans votre notice d'utilisation:<br>
- Une fois par an si le vid&eacute;oprojecteur fonctionne dans un environnement sain.<br>
- Tous les 3 mois si le vid&eacute;oprojecteur fonctionne dans un environnement poussiereux ou si vous fumez.<br>
N'utilisez pas de bombe &agrave; air comprim&eacute; vers l'int&eacute;rieur de l'appareil, vous risqueriez de disperser la poussi&egrave;re.
<br><br>
<b>La dur&eacute;e de vie d'une lampe</b><br>
La dur&eacute;e de vie de la lampe de votre projecteur est donn&eacute; &agrave; titre indicatif par le fabricant et c'est un temps de vie moyen (en mode &eacute;conomique si disponible). Elle durera plus longtemps si vous respectez le temps de refroidissement (voir ci-dessus <b>Eteindre le projecteur</b>) et les pr&eacute;cautions d'usage. Elle durera moins longtemps dans un environnement poussi&egrave;reux ou de fum&eacute;e de cigarette, pensez donc &agrave; nettoyer r&eacute;guli&egrave;rement le filtre du projecteur.<br>
Afin d'assurer une dur&eacute;e de vie plus longue de la lampe du projecteur, veillez &agrave; ne jamais couper l’alimentation du vid&eacute;oprojecteur pendant la phase de refroidissement apr&egrave;s extinction de la lampe, ventilateur en fonctionnement. L'utilisation d'un onduleur peut &egrave;tre tr&egrave;s utile si vous subissez souvent des coupures de courant &eacute;lectrique, afin de ne pas couper l'alimentation du ventilateur du projecteur.
<br><br>
<b>Le compteur horaire de votre projecteur</b><br>
Il vous indiquera la dur&eacute;e d'utilisation en heures et donc le moment de changer la lampe. Surtout ne pensez pas pouvoir faire des &eacute;conomies en essayant d'aller plus loin : la lampe pourrait imploser dans le projecteur et les d&eacute;gats ne seraient pas couverts par la garantie.
<br><br>
<b>La fin de la lampe</b><br>
Une luminosit&eacute; en baisse et une teinte de l’image jaune sont les signes de fin de vie d'une lampe. Il faut donc penser &agrave; la remplacer, m&ecirc;me si elle n'a pas attenteint la dur&eacute;e de vie pr&eacute;vue.<br>
A ce moment, si la lampe n’est pas chang&eacute;e, elle risque de finir par exploser, l'ampoule de verre ne supportant plus la temp&eacute;rature des &eacute;lectrodes qui se sont rapproch&eacute;es des extr&eacute;mit&eacute;s de la lampe. Cette explosion n’endommagera pas le projecteur si les d&eacute;bris de verre sont soigneusement nettoy&eacute;s.<br><br>


</p>
