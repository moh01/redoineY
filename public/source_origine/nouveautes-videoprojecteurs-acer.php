<b>Le pico-projecteur Acer C110 - Une projection " unique " via l'USB et un design ultral&eacute;ger</b>
<br><br>
Les images, les vid&eacute;os et les pr&eacute;sentations jouent un r&ocirc;le de plus en plus important dans nos vies, aussi bien dans le cadre priv&eacute; que professionnel. Nous utilisons nos smartphones et nos appareils photo pour prendre des photos ou filmer des vid&eacute;os, puis nous voulons les montrer &agrave; notre famille et &agrave; nos amis. Au travail, nous cr&eacute;ons des pr&eacute;sentations que nous partageons avec nos coll&egrave;gues. C'est pourquoi nous avons besoin des meilleurs instruments pour profiter de tous ces formats multim&eacute;dia et les partager.
<br><br>
Avec le pico-projecteur C110, Acer a cr&eacute;&eacute; le compagnon id&eacute;al et compl&eacute;mentaire pour son ordinateur portable. Non seulement le nouveau pico-projecteur est extr&ecirc;mement l&eacute;ger et ultra compact, mais il ne requiert pas l'usage d'un transformateur ni la pr&eacute;sence d'un port d'affichage. Il suffit d'un simple c&acirc;ble USB pour le connecter &agrave; son ordinateur portable et l'alimenter tout en permettant de regarder ses films, ses photos ou de projeter ses pr&eacute;sentations.
<br><br>
Le pico-projecteur C110 est con&ccedil;u pour s'adapter parfaitement au style de vie mobile. Non seulement il fonctionne avec un simple c&acirc;ble USB - donc plus de transformateur, plus de c&acirc;bles enchev&ecirc;tr&eacute;s et plus de probl&egrave;mes de prises de courant - mais il ne p&egrave;se que quelques grammes (175 g) pour une taille de 110x85x25 mm, pour une mobilit&eacute; sans limite. Son design noir lui conf&egrave;re une &eacute;l&eacute;gance sophistiqu&eacute;e.
<br><br>
La fonction Display over USB permet une utilisation d'une extr&ecirc;me facilit&eacute;, car il suffit de connecter le projecteur au portable pour afficher imm&eacute;diatement le contenu souhait&eacute;. De plus, la technologie Acer SmartFormat prend en charge une vaste gamme de formats PC ainsi les utilisateurs n'ont plus &agrave; se pr&eacute;occuper du r&eacute;glage de la r&eacute;solution du portable.
<br><br>
L'utilisateur pourra profiter de son multim&eacute;dia avec une taille d'affichage comprise entre 30 cm
(12 pouces) et 254 cm (100 pouces). Le contraste de 1000:1, la r&eacute;solution WVGA (854x480) et un format 16:9 natif garantissent des projections d'une grande nettet&eacute; et lumineuses avec une qualit&eacute; d'image vraiment impressionnante.
<br><br>
Pour obtenir la meilleure projection possible en mobilit&eacute;, le C110 peut &ecirc;tre accompagn&eacute; de l'&eacute;cran de projection pliable Acer en option. Sa principale caract&eacute;ristique, c'est qu'il n'est pas encombrant puisque d&egrave;s qu'il est rang&eacute;, sa taille n'exc&egrave;de pas celle d''un classeur. De plus, lorsqu'il est d&eacute;ploy&eacute;, il offre une surface de projection de 25 pouces favorisant les r&eacute;unions en groupe il am&eacute;liore la projection gr&acirc;ce &agrave; sa plus grande luminosit&eacute; (en multipliant par deux le reflet des &eacute;crans de projection standards).
<br><br>
Le pico-projecteur C110 d'Acer allie la technologie DLP &agrave; la technologie de l'ampoule LED, en am&eacute;liorant consid&eacute;rablement la qualit&eacute; de l'image. La technologie LED offre diff&eacute;rents avantages par rapport aux ampoules traditionnelles. Tout d'abord, le remplacement de l'ampoule devient pratiquement inutile &eacute;tant donn&eacute; que la dur&eacute;e de vie des LED est de 20 000 heures ; de plus, l'absence de parties cassables, comme les filaments ou les tubes, augmente la dur&eacute;e de vie. Pour finir, il peut obtenir un contraste et une saturation des couleurs plus &eacute;lev&eacute;s. Par cons&eacute;quent, les images affich&eacute;es sont plus &eacute;clatantes et r&eacute;alistes.
<br><br>
Le pico-projecteur C110 est le r&eacute;sultat concret de l'engagement continu d'Acer visant &agrave; cr&eacute;er des produits plus verts et plus &eacute;cologiques : la technologie d'ampoule LED ne contient pas de substances dangereuses comme le mercure ou l'halog&egrave;ne et par rapport &agrave; une ampoule traditionnelle elle peut r&eacute;duire la consommation d'&eacute;nergie de 30%.
<br>
 Avec le temps, les vid&eacute;o-projecteurs peuvent &ecirc;tre affect&eacute;s par une d&eacute;t&eacute;rioration des couleurs qui ajoute un ton jaun&acirc;tre ou verd&acirc;tre aux images projet&eacute;es &agrave; l'&eacute;cran. D&eacute;sormais les vid&eacute;o-projecteurs d'Acer b&eacute;n&eacute;ficient des technologies ColorSafe et DLP&reg; pour garantir l'int&eacute;grit&eacute; des couleurs m&ecirc;me apr&egrave;s une utilisation prolong&eacute;e, pour les rendre quasi insensibles &agrave; la d&eacute;t&eacute;rioration des couleurs.
<br><br>
Le pico-projecteur C110 permet de gagner un temps pr&eacute;cieux. Gr&acirc;ce &agrave; la fonction Quick Start, le pico-projecteur s'active dans les 15 secondes qui suivent le branchement du c&acirc;ble USB au portable ; la d&eacute;connexion se fait tout aussi rapidement. Avec Acer InstantPack, il n'est pas n&eacute;cessaire d'attendre le refroidissement, le projecteur peut &ecirc;tre &eacute;teint et rang&eacute; rapidement.
Le pico-projecteur C110 d'Acer est propos&eacute; avec une garantie standard de 2 ans retour atelier alors que la puce DLP est garantie 3 ans par Texas Instruments. 

