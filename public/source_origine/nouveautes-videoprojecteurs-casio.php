<h2>Nouveaut&eacute;s Videoprojecteurs <font color="#FF7F00"><? echo $marque ?></font> 2011</h2></td></tr>

<tr><td>

<div align="center"><b>29 mars 2011 : CASIO lance 12 nouveaux projecteurs, dans 3 cat&eacute;gories</b> - Les nouveaux vid&eacute;oprojecteurs disposent du Syst&egrave;me de Source Lumineuse Hybride</b></div>
<br><br>

TOKYO, le 29 mars 2011 — CASIO Computer Co., Ltd., a annonc&eacute; aujourd'hui le futur
lancement de 12 nouveaux vid&eacute;oprojecteurs dans 3 cat&eacute;gories, tous &eacute;quip&eacute;s de la source
lumineuse hybride laser et LED originale de CASIO qui associe lasers et LED (voir la description
ci-dessous). La nouvelle gamme comporte deux mod&egrave;les de 3.500 lumen ANSI, appartenant &agrave; la
s&eacute;rie Pro* (XJ-H1650 et XJ-H1600), deux mod&egrave;les de courte port&eacute;e*, particuli&egrave;rement appropri&eacute;s
&agrave; un usage &eacute;ducatif, et huit mod&egrave;les standards** de vid&eacute;oprojecteurs.<br><small>

* Lancement sur le march&eacute; des s&eacute;ries Pro et Courte Port&eacute;e pr&eacute;vu mi-juin 2011<br>
** Lancement sur le march&eacute; de la s&eacute;rie Standard pr&eacute;vu en Mai 2011</small><br><br>

CASIO a lanc&eacute; en avril 2010 le premier projecteur utilisant sa source lumineuse hybride laser et
LED originale. Les projecteurs de la gamme &agrave; succ&egrave;s Green Slim parvenaient &agrave; une luminosit&eacute;

&eacute;lev&eacute;e de 2.500 lumen ANSI sans utiliser de lampe contenant du mercure, une substance ayant
un effet n&eacute;faste sur l'environnement. En novembre 2010, CASIO a franchi l'&eacute;tape suivante en
utilisant la nouvelle source lumineuse dans un nouveau mod&egrave;le de 3.000 lumen ANSI. Depuis,
ce mod&egrave;le a continu&eacute; &agrave; gagner une solide r&eacute;putation en tant que projecteur de nouvelle
g&eacute;n&eacute;ration.<br><br>

Tous les mod&egrave;les pr&eacute;sentent les mêmes excellentes caract&eacute;ristiques de la source lumineuse
hybride laser et LED, y compris la dur&eacute;e de vie de la source lumineuse d'environ 20.000 heures
et la mise sous tension et hors tension rapide. Ces mod&egrave;les de grande qualit&eacute; disposent

&eacute;galement de diverses fonctions innovantes, telles que le contrôle intelligent de luminosit&eacute;, qui
r&egrave;gle automatiquement la luminosit&eacute; en fonction des conditions d'&eacute;clairage de l'environnement et
la capacit&eacute; &agrave; projeter des images en 3D, de même qu'une interface enti&egrave;rement raccordable.
<ul>
<li>La source lumineuse hybride laser & LED permet une projection ultra lumineuse sans
mercure, avec une source lumineuse de longue dur&eacute;e et une mise sous tension et hors
tension rapide</li>
<li>Contrôle intelligent de luminosit&eacute; qui r&egrave;gle automatiquement le niveau de luminosit&eacute; en
fonction des conditions d'&eacute;clairage environnantes</li>

<li>Les pr&eacute;sentations peuvent être propos&eacute;es en 3D en utilisant la projection DLP 3D*1</li>
<li>L'interface est &eacute;quip&eacute;e de trois types d'entr&eacute;es et de sorties vid&eacute;os et audios de même que
de terminaux HDMI, RGB et RS-232</li>
<li>Les smartphones*2 et les PC peuvent être connect&eacute;s sans fil</li>
<li>Les pr&eacute;sentations peuvent être propos&eacute;es sans PC, en utilisant la fonction d'accueil USB
2.0</li>

<li>La fonction de pointage interactif permet de commander un PC en pointant l'&eacute;cran*3</li>
</ul><small>Fonctions disponibles sur certains mod&egrave;les ; voir les sp&eacute;cifications produit d&eacute;taill&eacute;es.</small><br><br>

De plus, les mod&egrave;les de la nouvelle gamme pro (XJ-H1650 et XJ-H1600) am&eacute;liorent de fa&ccedil;on
impressionnante la luminosit&eacute; jusqu'&agrave; 3.500 lumen ANSI.<br>

<small>*1. Regarder une projection en 3D n&eacute;cessite des lunettes 3D CASIO pour projecteur CASIO, vendues s&eacute;par&eacute;ment. La projection 3D n'est pas possible en utilisant un terminal d'entr&eacute;e HDMI.<br>
*2. Pour une utilisation avec les smartphones ayant un r&eacute;seau LAN sans fil (IEEE 802.11 b/g) et disposant du logiciel MobiShow.<br>
*3. Cette fonction n&eacute;cessite le pointeur interactif CASIO, vendu s&eacute;par&eacute;ment.</small>
<br><br>

<b>Principales caract&eacute;ristiques des nouveaux projecteurs CASIO</b><br><br>

<b>Source lumineuse hybride laser & LED</b><br>
Les nouveaux projecteurs CASIO parviennent &agrave; une luminosit&eacute; &eacute;lev&eacute;e sans utiliser une lampe &agrave;
mercure haute pression, cette derni&egrave;re substance ayant un impact n&eacute;faste sur l'environnement.
Ils projettent trois couleurs par le biais d'une puce DLP - le rouge &eacute;mis par une LED rouge ultra
lumineux, le vert converti de la lumi&egrave;re laser bleue et le bleu &eacute;mis par une LED bleu ultra
lumineux.* La source de lumi&egrave;re a une dur&eacute;e de vie d'environ 20.000 heures, et permet ainsi de
r&eacute;duire la maintenance et les coûts d'exploitation. De plus, les projecteurs atteignent leur
luminosit&eacute; maximale en moins de cinq secondes, et ne n&eacute;cessitent pas de temps de
refroidissement lorsqu'on les met hors tension.<br><small>* La lumi&egrave;re laser bleue est utilis&eacute;e sur les mod&egrave;les standards de la gamme XJ-M.</small><br><br<>

<b>Contrôle de luminosit&eacute; intelligent</b><br>
Les projecteurs disposent d'un syst&egrave;me de contrôle intelligent de la luminosit&eacute;, qui utilise des
capteurs de lumi&egrave;re int&eacute;gr&eacute;s lui permettant de r&eacute;gler automatiquement le niveau de luminosit&eacute;
de la projection en fonction des conditions d'&eacute;clairage dans le milieu environnant. Cela permet
ainsi de minimiser la consommation d'&eacute;nergie, en utilisant uniquement le niveau de luminosit&eacute;
r&eacute;ellement n&eacute;cessaire.<br><br>

<b>Projection 3DDLP®</b><br>
En utilisant une technique s&eacute;quentielle d'images de grande qualit&eacute;, les projecteurs sont en
mesure de projeter des images en 3D* cr&eacute;&eacute;es avec le logiciel CASIO 3D Converter (vendu
s&eacute;par&eacute;ment), visionnables &agrave; l'aide de lunettes 3D adapt&eacute;es aux projecteurs CASIO (&eacute;galement
vendues s&eacute;par&eacute;ment).<br><small>* La projection 3D n'est pas possible en utilisant un terminal d'entr&eacute;e HDMI.</small><br><br>

<b>Interface enti&egrave;rement raccordable</b><br>
Tous les mod&egrave;les sont &eacute;quip&eacute;s d'une entr&eacute;e RGB, d'une entr&eacute;e vid&eacute;o, de terminaux HDMI et
RS-232C. Les mod&egrave;les de grande qualit&eacute; de la gamme XJ-H et les mod&egrave;les courte port&eacute;e de la
gamme XJ-ST sont fournis avec deux types d'entr&eacute;es RGB et une sortie RGB. Il existe

&eacute;galement un port pour câble LAN sur les mod&egrave;les &agrave; communication sans fil comprenant les
XJ-H1650, XJ-ST155, XJ-ST145, XJ-M255, XJ-M245, XJ-M155 et XJ-M145.
<br><br>

<b>Communication sans fil avec les smartphones et les ordinateurs (XJ-H1650, XJ-ST155,
XJ-ST145, XJ-M255, XJ-M245, XJ-M155 et XJ-M145)</b><br>
Des pr&eacute;sentations sans fil peuvent être effectu&eacute;es en utilisant des smartphones et d'autres
appareils mobiles &eacute;quip&eacute;s de r&eacute;seau local LAN sans fil (IEEE 802.11 b/g) et dot&eacute;s du logiciel
MobiShow ® * et, bien sûr, des ordinateurs avec les mêmes capacit&eacute;s LAN sans fil.<br><small>* Le logiciel MobiShow® est disponible dans des versions pour AndroidTM, Apple OS et Windows Mobile®. Les types de fichiers qui peuvent être utilis&eacute;s dans des pr&eacute;sentations sans fil peuvent être limit&eacute;s en fonction du syst&egrave;me d'exploitation utilis&eacute;.</small><br><br>

<b>Fonction d'accueil USB 2.0 (XJ-H1650, XJ-ST155, XJ-ST145, XJ-M255, XJ-M245, XJ-M155
et XJ-M145)</b><br>
Ces mod&egrave;les peuvent être utilis&eacute;s sans ordinateur, par simple connexion d'une cl&eacute; USB
(disponible dans le commerce) au projecteur pour visualiser des pr&eacute;sentations, des feuilles de
calcul, des documents de traitement de texte, des fichiers PDF, et d'autres donn&eacute;es* pr&eacute;par&eacute;es
sur un ordinateur, ainsi que des images fixes au format JPEG et BMP et des images en
mouvement au format Motion JPEG AVI.<br>
<b>* Les fichiers doivent être convertis en utilisant le logiciel PC fourni avec le projecteur et disponible en t&eacute;l&eacute;chargement sur les sites Web Casio.</b><br><br>

<b>Fonction de pointage interactif (XJ-H1650, XJ-ST155 et XJ-ST145)</b><br>
En utilisant le pointeur interactif de CASIO (vendu s&eacute;par&eacute;ment), un PC connect&eacute; peut être
actionn&eacute; en pointant l'&eacute;cran de projection. Il est &eacute;galement possible d'&eacute;crire sur l'&eacute;cran, &agrave;
condition que cette fonction soit utilis&eacute;e avec le logiciel Pr&eacute;sentation Draw, livr&eacute; avec le pointeur
Interactif.<br><small>• MobiShow est une marque d&eacute;pos&eacute;e d'AWIND Inc &agrave; Taiwan et/ou dans d'autres pays.<br>

• Android est une marque commerciale de Google Inc.<br>
• Apple iOS est une marque commerciale d'Apple Inc.<br>
• Windows Mobile est une marque d&eacute;pos&eacute;e de Microsoft Corporation aux &Eacute;tats-Unis et/ou dans d'autres pays.</small><br><br>

