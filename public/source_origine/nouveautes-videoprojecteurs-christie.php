A la différence des projecteurs actuels utilisant la technologie LCoS, le Christie D4K35 produit des blancs plus éclatants, des noirs plus profonds et bénéficie d'une meilleure durée de vie, tout en proposant une résolution quatre fois supérieure aux projecteurs 2K, évitant ainsi d'avoir recours à des systèmes couplés 2x2.
<br><br>
Caractéristiques principales
<br>
    * Luminosité de 35,000 lumens ANSI<br>
    * Quatre fois la résolution du 2K (4096 x 2160)<br>
    * Taux de contraste de 2000:1 pour une clarté d'image exceptionnelle<br>
    * Technologie Tri DLP® fournissant une durée de vie plus longue et une reproduction de couleurs plus riche que les autres technologies 4K<br>
    * Manutention simple : zoom et mise au point par télécommande, moteur optique nettoyable assurant un coût d'entretien faible<br>
<br><br>

