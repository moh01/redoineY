<h1>Le 4K devient une réalité pour les professionnels gr&acirc;ce au nouveau Christie D4K35</h1>

Christie est fière de vous annoncer l'arrivée du nouveau Christie D4K35, le premier projecteur 4K DLP® au monde con&ccedil;u tout spécialement pour une utilisation commerciale. Visant des applications demandant un très grande résolution et une luminosité extrême comme les parcs d'attractions, les grandes salles de conférence, les musées et planétariums, la simulation, la réalité virtuelle et le design automobile utilisant CAD, le projecteur Christie délivre 35000 lumens.
