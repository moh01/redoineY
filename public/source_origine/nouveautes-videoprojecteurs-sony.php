<H1>Vivez une expérience 3D de grande qualit&eacute;</H1>

Les projecteurs Home Cinema 3D offrent des images 3D de grande qualité à l'aide de la technologie 3D Active Shutter spécialisée de Sony. Regardez les derniers films 3D avec un projecteur 3D et recréez le cinéma en trois dimensions dans votre salon.
<br><br>
<b>Comment les projecteurs Home Cinema 3D de Sony fonctionnent-ils ?</b><br>
La technologie 3D de Sony réduit les interférences entre les images. Une précision ultrarapide pour de vraies images en 3D
<br>
Les projecteurs Home Cinema 3D de Sony possèdent une technologie 3D entièrement intégrée qui utilise le taux de rafraîchissement élevé de Motionflow, ainsi que les lunettes spéciales Active Shutter 3D (fournies avec le projecteur). Les projecteurs Home Cinema 3D utilisent un signal infrarouge pour synchroniser les images et les lunettes Active Shutter 3D avec précision à une vitesse élevée de 240 images par seconde. Résultat : des images Full HD 3D de grande qualité avec peu d'effet « fantôme » ou de « dédoublement » par rapport aux autres technologies 3D.
<br>
<b>Immersion totale dans les images 3D grâce aux lunettes 3D spéciales</b><br>

Les lunettes 3D de Sony utilisent la technologie Active Shutter : chaque verre est rempli de cristaux liquides qui se ferment pendant une fraction de seconde pour permettre l'envoi d'images consécutives différentes à l'œil gauche et à l'œil droit. En permettant à un seul œil de voir l'image Full HD qui lui est destinée, vous plongez dans le réalisme des images 3D de votre grand écran.
<br><br>
<b>Principaux avantages de la technologie 3D de Sony</b><br>

<u>Images Full HD 1080</u><br>

La technologie 3D utilise des panneaux SXRD™ hautes performances pour reproduire des images Full HD 1080 d'une couleur et d'une tonalité exceptionnelles sur grand écran. Comparez-la aux autres technologies 3D et vous remarquerez une nette différence en termes de qualité d'image.
<br>
<u>Paramètres réglables</u><br>

La technologie de projecteur 3D de Sony permet un meilleur contrôle de l'image 3D. Réglez la luminosité et la profondeur 3D en fonction des conditions ambiantes ou de vos préférences pour profiter au maximum de vos films en trois dimensions.
<br>
<u>Emetteur 3D intégré</u><br>

Les projecteurs possèdent un émetteur 3D entièrement intégré pour permettre un visionnage agréable depuis n'importe quel emplacement de la pièce, grâce à une distance de visualisation étendue de 5 mètres. Des tailles d'écran plus grandes sont également disponibles avec un émetteur optionnel.
<br>
<u>Conversion de la 2D en 3D</u><br>

Sony montre la voie avec une sélection de films, de sports et d'émissions TV en Full HD 3D toujours croissante. Nos projecteurs 3D convertissent également les images 2D en 3D pour offrir une dimension toute nouvelle à tous vos classiques.
