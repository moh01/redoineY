<tr><td colspan="2"><h2>Le videoprojecteur et sa lampe</h2></td></tr>

<tr>
<td><font color="#FF7F00">Le projecteur expliqu&eacute;</font><br><br>

<p align="justify">

<b>Les vidéoprojecteurs DLP/DMD</b><br>
 Ils reposent sur la technologie DLP (Digital Light Processing&trade;) développée par Texas Instruments, où chaque pixel correspond à un micro-miroir actionné par un champ électrique : la partie active peut être intégrée dans une puce DMD (Digital Micromirror Device). Chacun de ces miroirs renvoie ou non la lumière de la lampe vers l'écran. Le rapport cyclique de cet état donne la luminosité de chaque pixel variable de 0 à 100 %. En outre, un filtre tricolore RVB en rotation, situé entre la matrice DLP et la lampe, permet de projeter successivement les trois composantes de l'image finale.
<br>
Les avantages sont le contraste, l'absence de rémanence, la luminosité, l'absence de pixelisation, le rendu des teintes foncées, les réglages.
<br>
Les inconvénients sont la perte lumineuse et la fatigue oculaire dues au disque coloré, et la perception par certaines personnes de petits "flashes" d'arc-en-ciel pendant la projection. C'est le "rainbow effect". La technologie Tri DLP qui consiste à utiliser trois puces (une pour chaque couleur primaire) permet de supprimer ces problèmes. Les autres inconvénients sont les prix plus élevés que les LCD , la qualité d'image perfectible en référence aux tritubes (sauf tri-DLP 1 080 x 1 920 pixels), le bruit et la chaleur du système de refroidissement, la durée de vie de la lampe.
<br>
La durée de vie de la lampe est directement fonction de la qualité technique de l'appareil. Toshiba par exemple annonce des durées de vie de 2 000 heures alors que dans la pratique les lampes ne fonctionnent pas plus de 200 heures. Cela résulte d'une mauvaise conception de l'appareil et non du choix de la technologie. Les appareils bien conçus mettent en veille la lampe lorsque la température critique est atteinte. Cela permet de prolonger la durée de vie de la lampe.

<br><br>

<b>Les vidéoprojecteurs Tritubes (CRT)</b>
<br>
Popularisés par la société Barco, ces appareils se composent de trois tubes cathodiques de petite taille (typiquement 5.5" 6.5" 7" 8" et 9") à haute résolution et haut rendement, un tube pour chaque couleur primaire (rouge, vert, bleu). Chacun de ces tubes, au format 4/3, possède son propre objectif et l'image finale est obtenue par superposition des trois images primaires (synthèse additive) que l'on doit faire converger et déformer (correction de trapèze, de ballon) en raison de la position différente de chacun des tubes.
<br>
Ils ont pour avantages d'être dénués de structures visibles à l'écran (contrairement a la grille des écrans CRT ou des LCD) hormis la structure de l'image même (lignes visibles si elles sont en faible nombre). Les couleurs sont très fidèles si l'appareil est bien réglé et le taux de contraste mesuré sur le tube oscille entre 15 000:1 et 30 000:1 suivant les modèles (mesure qui chute en fonction de la clarté de la salle). Les plus petites tailles (7" comme le SD130 ou SD187 et 07ms ) d'où leur utilisation sur des machines de petites tailles comme les Barco serie 7xx ou 6xx ainsi que sur les rétroprojecteurs de la serie rd708 avec des images dune définition pour sur certainnes machines à focus electromagnetique pouvant depasser les 1280x1024 (nec 9pgxtra).
<br>
Les 8" sont compatibles DATA et GRAPHICS. Tous les 8" et 9" sont compatibles avec les fréquences TV, HDTV et DATA/GRAPHICS (dans la limite de leurs circuits électroniques) et peuvent se voir adjoindre un multiplicateur de ligne (line doubleur video scaler en anglais voir quadrupleur) pour améliorer les sources vidéo de faible définition (Pal, Secam, NTSC). Plus les tubes sont de grande taille et plus ils sont lumineux et définis.
<br>
7" (768 x 1 024), 8"focus électrostatique (1 200 x 1 500 1989-1996), 8" focus électromagnétique (1 200 x 1 600 Barco 1996-2002) (1 250 x 1 600 Barco 2002-2007) (1 200 x 1 700 Sony 1996-2002), 9" focus électromagnétique (1 600 x 2 000 Sony 1994-1998) (2 000 x 2 500 Barco-Sony-Electrohome 1998-2007)
<br>
Les modèles les plus sophistiqués donnent des images d'un naturel comparable à la pellicule de cinéma.
<br>
Les inconvénients majeurs de cette technique, qui est la plus ancienne, sont dans son rapport poids/encombrement/luminosité très faible ainsi que dans la complexité des réglages de convergences électroniques et mécaniques pour superposer au mieux ces trois images, réglages qui se compliquent avec l'augmentation de la résolution, entraînant inévitablement un surcoût devenu tel que les constructeurs ont décidé d'abandonner cette technologie. On peut citer également une luminosité non uniforme (réglable sur les 9" et certains 8"), le contraste qui ne peut s'apprécier que dans une salle noire a cause de la faible luminosité des machines (ce qui limite aussi la taille de l'écran à 2-3 mètres de base), le scintillement des lignes pour les sources en vidéo entrelacée ou les images en faible cadence (moins de 45 Hz), un recul important nécessaire avec les sources de qualités moyennes, le marquage du phosphore avec des images fixes intenses au bout de quelques dizaines d'heures.
<br>
Le prix du neuf en 2002 : le petit Barco 508, 7"compatible TV, coûtait 7 000 euros ; le gros Barco 909, 9"compatible TV-HDTV-DATA coûtait 100 000 euros
<br>
Les principaux constructeurs : <a href="reference-lampe-videoprojecteur-Barco.html">Barco</a>, <a href="reference-lampe-videoprojecteur-Electrohome.html">Electrohome</a>, <a href="reference-lampe-videoprojecteur-Mitsubishi.html">Mitsubishi</a>, <a href="reference-lampe-videoprojecteur-Nec.html">Nec</a>, <a href="reference-lampe-videoprojecteur-Panasonic.html">Panasonic</a>, <a href="reference-lampe-videoprojecteur-Seleco.html">Seleco</a> (<a href="reference-lampe-videoprojecteur-Sim2.html">Sim2</a>), <a href="reference-lampe-videoprojecteur-Sony.html">Sony</a>.

<br><br>

<b>Les vidéoprojecteurs Laser (LDP)</b>
<br>
La technique la plus ancienne est celle du balayage à deux axes X-Y d'un faisceau laser sur un ou deux miroirs montés sur galvanomètres, similaire à la méthode de balayage d'un tube cathodique, elle présente l'inconvénient d'offrir une faible définition (moins de 100 lignes) due à la lenteur des galvanomètres d'où une utilisation limitée aux spectacles et dans les discothèques avec des images très géométriques en "fils de fer". Mais c'est aussi celle retenue pour des applications nomades (picoprojecteur) comme l'intégration dans des PDAs ou des téléphones portables de systèmes de projections monochromes ou couleurs ultra miniaturisés grâce aux technologies MEMs. La miniaturisation des éléments permet une grande vitesse de balayage rendant possible l'affichage de signaux HDTV. La société Arasor en fait la démonstration sur un prototype de rétroprojecteur en 2007.

<br><br>

<b>Le contraste</b>
<br>
Exprimé sous la forme d'un quotient "valeur en lux : 1", soit la valeur en lux d'un signal blanc à 100 % moins valeur en lux d'un signal blanc à 0 %, le tout divisé par la valeur en lux d'un signal blanc à 0 % (un signal blanc à 0 % correspond à du noir).
<br>
Le taux de contraste est une des caractéristiques essentielles à prendre en compte dans le choix d'un vidéoprojecteur. Plus le taux de contraste est élevé, plus la qualité de l'image est optimale. Dans le choix d'un projecteur Home cinema, bien plus que la luminosité c'est le taux de contraste qui assure la qualité de l'image. Un taux de contraste élevé permettra d'obtenir plus de détails dans les nuances et a priori des noirs plus profonds. Cela donne du relief à l'image et accentue ainsi la sensation de trois dimensions.
<br>
En Home cinema, un bon de taux de contraste doit se situer entre 2 000:1 et 4 000:1 en vidéo (à 6 500 K).

<br><br>

<b>La luminosité</b>
<br>
Exprimée en lumen :
<br>
La puissance lumineuse s'exprime généralement en lumens ANSI, unité de mesure normée par l'American National Standards Institute. Elle va dépendre du type d'utilisation. Plus la taille de l'image souhaitée est grande et la luminosité ambiante importante, plus le projecteur doit être lumineux.
<br>
Utilisation Professionnelle 
Tout dépend du type d'utilisation et en particulier du type de salle. Pour des présentations dans de petites salles de réunion, un projecteur avec 1 500 lumens suffira largement. En revanche, dans des grands salles de conférences très éclairées, avec des écrans de grande taille, le projecteur doit être nettement plus lumineux pour projeter une image claire et lisible (au moins 2 500 lumens).
<br>
Utilisation Home cinema 
Dans le cadre d'une utilisation Home cinema, la taille de l'écran est généralement comprise entre 1,5 et 3 mètres de base. Comme dans un vrai cinéma, la qualité d'image est la meilleure lorsque l'obscurité est la plus complète possible. Pour obtenir une image d'excellente qualité, le projecteur doit afficher une luminosité comprise entre 600 et 1 000 lumens.

<br><br>

<b>La définition</b>
<br>
Exprimée en pixels :
<br>
Que ce soit en LCD ou DLP, chaque matrice est constituée de pixels, éléments de base de l'image. La définition de l'image va dépendre du nombre de pixels : plus ils sont nombreux plus la précision de l'image est censée être élevée.
<br>
Il convient néanmoins de choisir la définition de son projecteur en fonction de son besoin d'utilisation. Et les besoins sont très différents selon qu'on projette essentiellement de l'image informatique (présentations type Powerpoint, tableurs, etc.) ou de la vidéo (Home cinema).

<br><br>

<b>Image informatique (utilisation professionnelle)</b>
<br>
Le critère majeur de choix est la définition qu'affiche l'ordinateur qu'on connecte au projecteur. Avec les PC portables notamment, il est plus confortable de disposer sur le projecteur de la même définition que sur l'écran interne.
<br>
Principales définitions Data (normes VESA) :
<br>
VGA : 640 x 480 <br>
SVGA : 800 x 600 <br>
XGA : 1 024 x 768 <br>
SXGA : 1 280 x 1 024 <br>
UXGA : 1 600 x 1 200 <br>
QXGA : 2 048 x 1 536 

<br><br>

<b>Image Vidéo (utilisation Home Cinéma)</b>
<br>
Le besoin de définition est moindre, du moins en Europe, puisque nos bonnes vieilles images analogiques PAL, SECAM, ou NTSC, sont de qualité et définition moins élevées que les images Data, une fois converties en numérique pour l'affichage sur un projecteur LCD ou DLP, qui nous le rappelons, sont des produits numériques. En outre, un autre critère est important à prendre en compte : le 4/3 ou le 16/9.
<br>
Définitions des principaux standards vidéos analogiques convertis en numériques :
<br>
PAL/SECAM en 4/3 : 768 x 576 <br>
PAL/SECAM en 16/9 et 576i : 1 024 x 576 <br>
NTSC en 4/3 : 720 x 480 <br>
NTSC en 16/9 et 480p : 848 x 480 <br>
Donc, globalement, si on se contente de regarder essentiellement de l'image télé classique en 4/3, un projecteur 4/3 de définition SVGA (800 x 600) suffira amplement, une définition plus élevée n'amenant pas un gain de qualité d'image significatif.
<br>
Si on regarde beaucoup de 16/9, une définition XGA (1 024 x 768) sera plus conseillée, surtout en PAL, ou alors un projecteur de matrice spécifique 16/9, dont l'offre s'élargit de plus en plus dans diverses définitions (848 x 480, 960 x 540, 1 024 x 576).
<br>
Si on a la chance de disposer d'une source TV HD, un projecteur équipée d'une matrice haute définition s'impose. Comme pour l'image data, les signaux TV HD sont des signaux graphiques de très haute définition et toujours de format 16/9, d'une qualité très supérieure au PAL, SECAM et NTSC, avec lesquels ils n'ont rien de commun.
<br>
Définitions des deux principaux standards TVHD (USA et Japon) :
<br>
720p : 1 280 x 720 <br>
1 080i : 1 920 x 1 080 (entrelacée) <br>
1 080p : 1 920 x 1 080 

<br><br>
<b>Le niveau sonore</b>
<br>
Exprimé en décibels pondérés (dBA) :
<br>
Le niveau sonore est dû au bruit généré par le ventilateur chargé de refroidir la lampe de l'appareil. Sur la plupart des documentations des constructeurs, le niveau sonore est sous-estimé ou mesuré à l'opposé du ventilateur. Il est donc fortement conseillé d'essayer l'appareil dans les conditions voulues (et non dans un magasin).
<br>
Il faut retenir qu'une augmentation de 3 dBA double l'intensité sonore.
<br>
Pour un usage domestique (dans un salon par exemple), une valeur de 25 dBA est parfaite, avec 28 dBA comme maximum.

</p>
